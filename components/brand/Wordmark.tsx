const FLAT_PERIOD = { cx: 769.2, cy: 181, r: 15 } as const;

const LETTER_TRANSFORM = "translate(40.00,-4.00)";

export function Wordmark({
  size = "md",
  variant = "default",
  className,
}: {
  size?: WordmarkSize;
  variant?: WordmarkVariant;
  className?: string;
}) {
  const px = SIZE_PX[size];
  const useFlat = px < 32;
  const viewBox = useFlat ? FLAT_VIEWBOX : DIMINUENDO_VIEWBOX;
  const paths = useFlat ? FLAT_PATHS : DIMINUENDO_PATHS;
  const period = useFlat ? FLAT_PERIOD : DIMINUENDO_PERIOD;
  const aspect = useFlat ? 824.2 / 236 : 796.8 / 236;
  // Letter color follows the wrapper class:
  // default → text-fg-1 (ink on bone). on-ink → text-bg-1 (bone on ink).
  const wrapperColor = variant === "on-ink" ? "text-bg-1" : "text-fg-1";
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox={viewBox}
      height={px}
      width={px * aspect}
      role="img"
      aria-label="Talental"
      className={cn(wrapperColor, className)}
    >
      <g fill="currentColor" transform={LETTER_TRANSFORM}>
        {paths.map((d, i) => (
          <path key={i} d={d} />
        ))}
      </g>
      <circle
        cx={period.cx}
        cy={period.cy}
        r={period.r}
        fill="var(--accent)"
      />
    </svg>
  );
}
